1.1 Elasticsearch
     1.1.1 ElasticSearch的历史 
        1.一个叫做Shay Banon的开发者为妻子构建了一个食谱的搜索引擎，构建了早期版本的Lucene
        2.ES的优点：
          2.1 横向可扩展：只需要增加一台机器，做一点配置，启动一下ES就能并入集群
          2.2 分片机制提供更好的分布性:
              同一个索引可以设置多个分片，类似HDFS的复制机制
          2.3 高可用：
              一个分片可以设置多个复制，某台服务器在宕机的情况下，集群能够照常运行，并且会把宕机丢失的数据复制到其他可用节点上
          2.4 使用简单:
              只需要一条命令就能下载文件，然后很快就能搭建一个站内搜索                             
